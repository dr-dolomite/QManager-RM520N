<table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="background-color:${_EA_COLOR_BG};padding:32px 16px;">
<tr><td align="center">
<table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="max-width:520px;">

<!-- Header -->
<tr><td style="background-color:${_EA_COLOR_PRIMARY};padding:24px 32px;border-radius:10px 10px 0 0;">
  <table role="presentation" width="100%" cellpadding="0" cellspacing="0">
  <tr>
    <td style="color:${_EA_COLOR_WHITE};font-size:20px;font-weight:700;letter-spacing:-0.3px;">QManager</td>
  </tr>
  <tr>
    <td style="color:rgba(255,255,255,0.75);font-size:13px;padding-top:4px;">${subtitle}</td>
  </tr>
  </table>
</td></tr>

<!-- Content card -->
<tr><td style="background-color:${_EA_COLOR_WHITE};padding:32px;border-radius:0 0 10px 10px;">
HTMLEOF
}

_ea_html_footer() {
    cat <<HTMLEOF
</td></tr>

<!-- Footer -->
<tr><td style="padding:24px 32px;text-align:center;">
  <p style="margin:0 0 4px;font-size:12px;color:${_EA_COLOR_MUTED};">QManager &middot; Quectel Modem Management</p>
  <p style="margin:0;font-size:11px;color:${_EA_COLOR_MUTED};">You received this because email alerts are enabled on your device.</p>
</td></tr>

</table>
</td></tr></table>
</body>
</html>
HTMLEOF
}

# --- Generic alert email (connection_restored / reboot / connection_lost) ----
# Single shared template for every alert_engine.sh-dispatched trigger. Badge
# label/color is chosen from the trigger keyword so recovery vs. reboot vs.
# a generic alert are visually distinct without needing a template per event.

_ea_build_generic_html() {
    local subject="$1"
    local body="$2"
    local badge_label="ALERT"
    local badge_color="$_EA_COLOR_PRIMARY"

    case "$subject" in
        *Recovered*) badge_label="RECOVERED"; badge_color="$_EA_COLOR_SUCCESS" ;;
        *Rebooted*)  badge_label="REBOOT";    badge_color="$_EA_COLOR_PRIMARY" ;;
    esac

    {
        _ea_html_header "QManager Alert"

        cat <<HTMLEOF
  <!-- Badge -->
  <table role="presentation" cellpadding="0" cellspacing="0"><tr>
    <td style="background-color:${badge_color};color:${_EA_COLOR_WHITE};font-size:12px;font-weight:600;padding:4px 12px;border-radius:20px;letter-spacing:0.3px;">${badge_label}</td>
  </tr></table>

  <!-- Headline -->
  <h1 style="margin:16px 0 8px;font-size:18px;font-weight:600;color:${_EA_COLOR_TEXT};">${subject}</h1>
  <p style="margin:0;font-size:14px;color:${_EA_COLOR_TEXT};line-height:1.5;">${body}</p>
HTMLEOF

        _ea_html_footer
    }
}

